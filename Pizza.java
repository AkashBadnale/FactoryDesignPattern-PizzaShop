package com.ab.pizzaShop;

public abstract class Pizza {

	public abstract void getSize();

	public abstract void getExtra();

	public abstract void getPaymentType();

}// PizzaHut

/*
 * Implementations of Pizza for all different Types of Pizza Each Pizza as
 * different class
 */
class Margherita extends Pizza {

	@Override
	public void getSize() {
		PizzaUtilityClass.size();
	}

	@Override
	public void getExtra() {
		PizzaUtilityClass.extra();
	}

	@Override
	public void getPaymentType() {
		PizzaUtilityClass.paymentType();
	}
}// Margherita

